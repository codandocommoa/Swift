 Ao contrário dos comentários de várias linhas em C, os comentários de várias linhas em Swift podem ser aninhados dentro de outros comentários de várias linhas. Você escreve comentários aninhados iniciando um bloco de comentários de várias linhas e, em seguida, iniciando um segundo comentário de várias linhas dentro do primeiro bloco. O segundo bloco é então fechado, seguido pelo primeiro bloco:
 */

