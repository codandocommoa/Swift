 Use comentários para incluir texto não executável em seu código, como uma nota ou lembrete para você mesmo. Os comentários são ignorados pelo compilador Swift quando seu código é compilado.

 Comentários em Swift são muito semelhantes aos comentários em C. Comentários de linha única começam com duas barras (//):

*/

